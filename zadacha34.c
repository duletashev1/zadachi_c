#include <stdio.h>
int proverka(int n){
    int vk;
    int p=1;
    for(int i=1;i<=n;i+=2){
        n-=p;
        vk++;
        p+=2;

    }
    if(n==0){
        return vk;
    }
    else return 0;
}


